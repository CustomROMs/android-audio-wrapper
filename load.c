#include <hardware/hardware.h>
#include <hardware/audio.h>
#include <dlfcn.h>
#include <errno.h>

#define LOG_TAG "AudioWrapper"
#include <cutils/log.h>

void *gHandle = NULL;

/**
 * Load the file defined by the variant and if successful
 * return the dlopen handle and the hmi.
 * @return 0 = success, !0 = failure.
 */
static int load_simple(const char *path,
        struct hw_module_t **pHmi)
{
    int status = -EINVAL;
    void *handle = NULL;
    struct hw_module_t *hmi = NULL;

    /*
     * load the symbols resolving undefined symbols before
     * dlopen returns. Since RTLD_GLOBAL is not or'd in with
     * RTLD_NOW the external symbols will not be global
     */
    handle = dlopen(path, RTLD_NOW);
    if (handle == NULL) {
        char const *err_str = dlerror();
        ALOGE("load: module=%s\n%s", path, err_str?err_str:"unknown");
        status = -EINVAL;
        goto done;
    }

    gHandle = handle;

    /* Get the address of the struct hal_module_info. */
    const char *sym = "HMI";
    hmi = (struct hw_module_t *)dlsym(handle, sym);
    if (hmi == NULL) {
        ALOGE("load: couldn't find symbol %s", sym);
        status = -EINVAL;
        goto done;
    }

    hmi->dso = handle;

    /* success */
    status = 0;

    done:
    if (status != 0) {
        hmi = NULL;
        if (handle != NULL) {
            dlclose(handle);
            handle = NULL;
        }
    } else {
        ALOGE("loaded HAL path=%s hmi=%p handle=%p",
                path, *pHmi, handle);
    }

    *pHmi = hmi;

    return status;
}

int hw_get_module_by_path2(char *path, const struct hw_module_t **module)
{
    return load_simple(path, module);
}
